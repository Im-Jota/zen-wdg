// ─── Helper: get translated string ────────────
const DEFAULT_LOCALE = 'es';

/**
 * Get localized strings for a widget
 * @param {string} widget - Widget key (clock, todo, weather, calendar, notes, marked, modals)
 * @param {string} locale - Locale code (es, en, fr, de, pt, it, ja, zh, ko)
 * @returns {Object} Translations object for the widget
 */
export function getWidgetLocale(widget, locale = DEFAULT_LOCALE) {
  const lang = locales[locale] || locales[DEFAULT_LOCALE];
  return lang[widget] || locales[DEFAULT_LOCALE][widget] || {};
}

/**
 * Get a single translated key using dot notation
 * @param {string} key - Dot-notation key like 'todo.add' or 'weather.descriptions.0'
 * @param {string} locale - Locale code
 * @param {Object} params - Optional interpolation params, e.g. { count: 5 }
 * @returns {string} Translated string
 */
export function t(key, locale = DEFAULT_LOCALE, params = {}) {
  const parts = key.split('.');
  let result = locales[locale] || locales[DEFAULT_LOCALE];
  
  for (const part of parts) {
    result = result?.[part];
    if (result === undefined) {
      // Fallback to default locale
      result = locales[DEFAULT_LOCALE];
      for (const p of parts) {
        result = result?.[p];
        if (result === undefined) return key;
      }
      break;
    }
  }
  
  if (typeof result === 'string' && params) {
    return result.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? _);
  }
  
  return result ?? key;
}

/**
 * Get all available locale codes
 * @returns {string[]}
 */
export function getAvailableLocales() {
  return Object.keys(locales);
}

